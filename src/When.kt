fun main() {

    // switch, when

    var dia = 7
    when(dia) {
        1 -> println("Lunes")
        2 -> println("Martes")
        3 -> println("Miercoles")
        4 -> println("Jueves")
        5 -> println("Viernes")
        6 -> println("Sabado")
        else -> println("El numero no es valido")
    }
}